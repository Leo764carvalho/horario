    private void formWindowOpened(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowOpened
        HorarioSingleton singleton = HorarioSingleton.getInstance();

        for (Turma turma : singleton.getTurmas()) {
            String turno = turma.getTurno() == 0 ? "Tarde" : "Noite";
            ((DefaultTableModel) tblTurma.getModel()).addRow(new Object[]{turma.getNome(), turno, turma.getSala()});
        }
    }//GEN-LAST:event_formWindowOpened

