    private void btnAdicionaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAdicionaActionPerformed
        String nome = txtTurma.getText();
        if (nome != null && !nome.isEmpty()) {
            int turno = ccTurno.getSelectedIndex();
            String sala = txtSala.getText();

            Turma turma = new Turma(nome);
            turma.setTurno(turno);
            turma.setSala(sala);
            HorarioSingleton singleton = HorarioSingleton.getInstance();
            singleton.getTurmas().add(turma);
            try {
                singleton.save();
            } catch (IOException ex) {
                Logger.getLogger(viewTurma.class.getName()).log(Level.SEVERE, null, ex);
            }

            ((DefaultTableModel) tblTurma.getModel()).addRow(new Object[]{nome, ccTurno.getSelectedItem(), sala});

            //Limpa os campos
            txtTurma.setText("");
            txtSala.setText("");
        }
    }//GEN-LAST:event_btnAdicionaActionPerformed

