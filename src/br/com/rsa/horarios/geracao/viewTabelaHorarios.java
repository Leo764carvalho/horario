    private void formWindowOpened(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowOpened
        HorarioSingleton singleton = HorarioSingleton.getInstance();
        Map<Aula, Horario> coloracao = singleton.getColoracao();
        if (coloracao != null) {
            DefaultTableModel model = (DefaultTableModel) tblHorarios.getModel();
            model.setColumnCount(5);
            model.addRow(new Object[]{"Segunda", "Ter√ßa", "Quarta", "Quinta", "Sexta"});
            for (Turma turma : singleton.getTurmas()) {
                model.addRow(new Object[]{turma});

                String[][] tabelaHorario = new String[2][];
                tabelaHorario[0] = new String[5];
                tabelaHorario[1] = new String[5];
                boolean vazio = true;
                for (Horario c : singleton.getTurnoTarde()) {
                    tabelaHorario[c.getHora() % 2][c.getDia().ordinal()] = "";

                    for (Map.Entry<Aula, Horario> par : coloracao.entrySet()) {
                        if (par.getValue().equals(c)) {
                            if (par.getKey().getDisciplina().getTurmas().contains(turma)) {
                                //aula = par.getKey();
                                tabelaHorario[c.getHora() % 2][c.getDia().ordinal()] = par.getKey().toString();
                                vazio = false;
                            }
                        }
                    }
                }
                if (!vazio) {
                    model.addRow(tabelaHorario[0]);
                    model.addRow(tabelaHorario[1]);
                }
                vazio = true;
                for (Horario c : singleton.getTurnoNoite()) {
                    tabelaHorario[c.getHora() % 2][c.getDia().ordinal()] = "";
                    for (Map.Entry<Aula, Horario> par : coloracao.entrySet()) {
                        if (par.getValue().equals(c)) {
                            if (par.getKey().getDisciplina().getTurmas().contains(turma)) {
                                //aula = par.getKey();
                                tabelaHorario[c.getHora() % 2][c.getDia().ordinal()] = par.getKey().toString();
                                vazio = false;
                            }
                        }
                    }
                }
                if (!vazio) {
                    model.addRow(tabelaHorario[0]);
                    model.addRow(tabelaHorario[1]);
                }
                model.addRow(new Object[]{});
            }
        }
        singleton.getCursos().get(0).imprimeHorario(coloracao);
    }//GEN-LAST:event_formWindowOpened

