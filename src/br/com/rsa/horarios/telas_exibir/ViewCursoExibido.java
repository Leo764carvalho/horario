    private void formWindowOpened(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowOpened
        HorarioSingleton singleton = HorarioSingleton.getInstance();

        if (!singleton.getCursos().isEmpty()) {
            Curso curso = singleton.getCursos().get(0);

            DefaultTableModel model = (DefaultTableModel) tblDadosCurso.getModel();
            for (Disciplina disc : curso.getDisciplinas()) {
                String cargaHoraria = "";
                switch (disc.getAulas()) {
                    case 1:
                        cargaHoraria = "36h";
                        break;
                    case 2:
                        cargaHoraria = "72h";
                        break;
                    case 3:
                        cargaHoraria = "108h";
                        break;
                }
                String turmasTxt;
                String salaTxt;
                List<Turma> turmas = disc.getTurmas();
                String turnoTxt;
                if (turmas.isEmpty()) {
                    turmasTxt = "N達o definida";
                    salaTxt = "N達o definida";
                    turnoTxt = "N達o definido";
                } else {
                    turmasTxt = turmas.get(0).toString();
                    salaTxt = turmas.get(0).getSala();
                    turnoTxt = turmas.get(0).getTurno() == 0 ? "Tarde" : "Noite";
                    for (int i = 1; i < turmas.size(); i++) {
                        turmasTxt += ", " + turmas.get(i).toString();
                    }
                }
                String professorTxt = disc.getProfessor() == null ? "N達o definido" : disc.getProfessor().toString();
                model.addRow(new Object[]{turmasTxt, professorTxt, disc, cargaHoraria, turnoTxt, salaTxt});
            }
        }
    }//GEN-LAST:event_formWindowOpened

